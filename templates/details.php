<h3 class="course-title">Introduction to Advertising</h3>
<div class="description-text scrollbar">
<p>The free online course Introduction to Advertising will introduce you to the process of advertising including how ad agencies work, the types of advertisements that can be used, and how to evaluate the effectiveness of an ad. The course follows the ad agency Shepardson, Stern, and Kaminsky (SS+K) as it works on an ad campaign for MSNBC. <br/><br/>
The course begins by introducing you to the pitch process, how to identify objectives for an ad campaign, and the methods that can be used for deploying an advertising message. You will learn about the Four Ps: product, place, price, and promotion, and how to blend these together to create the desired impact. Mostly particularly, you will learn what it was like for SS+K to go through these steps with MSNBC. <br/><br/>
Next, the course discusses methods for managing media such as how to use SWOT analysis and asymmetric communications briefs to solve problems. You will be introduced to the concept of virtual worlds and how advertisers can infiltrate them to bring messages to consumers.
Ad development is a key step and you will learn about copy research and copy testing, as well as relevance and resonance. The course reviews how to construct likable ads and how to appeal to consumers. You will learn methods for marketing a product or service, as well as the benefits of radio and print ads. You will also learn how to measure readership, about the diffusion of innovation, and the benefits of customer relationship management. <br/><br/>
This free online course will be of great interest to professionals working in the areas of advertising, marketing, sales and promotion, and who like to learn more about the advertising process. It will also be of interest to students of business and marketing, business owners seeking to promote their goods or services, or even someone who is simply interested in the process behind the promotion of the goods and services they consume.</p>
